var numero1 = parseFloat(prompt('Digite um numero inteiro: '));
var numero2 = parseFloat(prompt('Digite outro numero inteiro: '));

var soma = numero1 + numero2;
var sub = numero1 - numero2;
var mult = numero1 * numero2;
var div = numero1 / numero2;
var resto = numero1 % numero2; 

alert("Soma: " + soma);
alert("Subtração: " + sub);
alert("Multiplicação: " + mult);
alert("Divisão: " + div);
alert("Resto da divisão inteira: " + resto);